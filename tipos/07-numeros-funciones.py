import math  # Módulo math.py -> código python

print(round(1.3))  # Redondea al número más cercano
print(round(1.7))
print(round(1.5))
print(abs(-77))  # Devuelve del valor absuluto
print(abs(55))

print(math.ceil(1.1))  # Número superior entero más cercano
print(math.floor(1.9999))  # Númoer inferior entero más cercano
print(math.isnan(23))  # Corresponde a un número
# print(math.isnan("23"))
print(math.pow(10, 3))  # Elevar un númoer a la potencia de algo
print(math.sqrt(9))  # Sacar la raiz cuadrada de un número
